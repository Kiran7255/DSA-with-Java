// Write a Java mehod to compute the averate of three numbers.

public class firstQs {
    public static float Threeno(int x, int y, int z){
        float avg = (x+y+z)/3.0f;
        return avg;
    }

    public static void main(String args[]){
        System.out.print(Threeno(10, 40, 20));
    }
    
}
